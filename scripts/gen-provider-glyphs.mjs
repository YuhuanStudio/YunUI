// Generate src/ai/provider-glyphs.generated.ts — a small, bundled map of
// monochrome (fill=currentColor) brand glyphs for the common providers, pulled
// from @lobehub/icons-static-svg (MIT). These render INLINE so they inherit the
// text color (theme-adaptive) and sit on a uniform neutral tile — unlike the
// brand-colored avatar webps, whose baked-in backgrounds (OpenAI black, Anthropic
// cream) look disjointed next to each other and against the grayscale UI.
//
// Curated on purpose: only the providers a model picker actually shows, so the
// bundle stays small. Unknown providers fall back to the avatar/letter as before.
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const SVG = join(root, "node_modules/@lobehub/icons-static-svg/icons");

// lobe filename slugs (already normalized: lowercase, no separators).
const SLUGS = [
  "openai", "anthropic", "claude", "google", "gemini", "gemma", "deepseek",
  "qwen", "alibaba", "mistral", "meta", "grok", "xai", "cohere", "moonshot",
  "kimi", "zhipu", "chatglm", "minimax", "microsoft", "azure", "perplexity",
  "groq", "together", "openrouter", "ollama", "stepfun", "baidu", "wenxin",
  "tencent", "hunyuan", "bytedance", "doubao", "yi", "nvidia", "huggingface",
  "lmstudio", "fireworks", "siliconcloud", "volcengine", "ai21", "stability",
  "midjourney", "flux", "suno",
];

const out = {};
for (const slug of SLUGS) {
  let raw;
  try {
    raw = readFileSync(join(SVG, `${slug}.svg`), "utf8");
  } catch {
    console.warn(`[glyphs] missing ${slug}.svg — skipped`);
    continue;
  }
  const vb = (raw.match(/viewBox="([^"]+)"/) || [, "0 0 24 24"])[1];
  // inner = everything inside the outer <svg>…</svg>, minus the <title>.
  let body = raw
    .replace(/^[\s\S]*?<svg[^>]*>/, "")
    .replace(/<\/svg>\s*$/, "")
    .replace(/<title>[\s\S]*?<\/title>/g, "")
    // strip per-element fills so the whole glyph follows the svg's currentColor.
    .replace(/\s+fill="(?!none)[^"]*"/g, "")
    .trim();
  out[slug] = { vb, body };
}

const header = `// AUTO-GENERATED by scripts/gen-provider-glyphs.mjs — do not edit by hand.\n` +
  `// Monochrome brand glyphs (fill=currentColor) for the common providers.\n` +
  `// Source: @lobehub/icons-static-svg (MIT). Regenerate: node scripts/gen-provider-glyphs.mjs\n\n`;
const body =
  `export interface ProviderGlyph {\n  /** viewBox of the source SVG. */\n  vb: string;\n  /** Inner SVG markup (paths), fill inherits currentColor. */\n  body: string;\n}\n\n` +
  `export const PROVIDER_GLYPHS: Record<string, ProviderGlyph> = ${JSON.stringify(out, null, 2)};\n`;

const dest = join(root, "src/ai/provider-glyphs.generated.ts");
writeFileSync(dest, header + body);
console.log(`[glyphs] wrote ${Object.keys(out).length} glyphs -> ${dest}`);
